#include "fdf.h"

int				brightness(int color, float b)
{
	int			c;
	int			tmp;

	tmp = 0;
	c = (color & 255) * b;
	tmp = tmp | c;
	c = ((color >> 8) & 255) * b;
	tmp = tmp | (c << 8);
	c = ((color >> 16) & 255) * b;
	tmp = tmp | (c << 16);
	c = tmp;
	return (c);
}


